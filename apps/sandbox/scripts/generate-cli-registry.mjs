/**
 * Generates apps/sandbox/src/generated/cliRegistry.ts by introspecting
 * the @xds/cli Commander.js program after all commands register.
 *
 * This avoids manually maintaining CLI docs — the source of truth is
 * the actual CLI command registration code.
 */

import {Command} from 'commander';
import * as fs from 'node:fs';
import * as path from 'node:path';
import {fileURLToPath} from 'node:url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const REPO_ROOT = path.resolve(__dirname, '..', '..', '..');
const CLI_SRC = path.join(REPO_ROOT, 'packages', 'cli', 'src');
const OUT_DIR = path.resolve(__dirname, '..', 'src', 'generated');
const OUT_FILE = path.join(OUT_DIR, 'cliRegistry.ts');

const CLI_PKG = JSON.parse(
  fs.readFileSync(path.join(REPO_ROOT, 'packages', 'cli', 'package.json'), 'utf-8'),
);

// ── Build a fresh Commander program mirroring the real CLI ──────────

const program = new Command();
program
  .name('xds')
  .description(CLI_PKG.description)
  .version(CLI_PKG.version)
  .option('--zh', 'Output docs in Chinese Simplified')
  .option('--dense', 'Output docs in compressed dense format (token-efficient)')
  .option('--lang <locale>', 'Output docs in specified language/format (en, zh, dense)')
  .option('--detail <level>', 'Output detail level (full, compact, brief)', 'full')
  .option('--json', 'Output as typed JSON (envelope: { type, data })');

const commandModules = [
  {name: 'init', path: './commands/init.mjs', register: 'registerInit'},
  {name: 'component', path: './commands/component/index.mjs', register: 'registerComponent'},
  {name: 'docs', path: './commands/docs.mjs', register: 'registerDocs'},
  {name: 'swizzle', path: './commands/swizzle.mjs', register: 'registerSwizzle'},
  {name: 'template', path: './commands/template.mjs', register: 'registerTemplate'},
  {name: 'gap-report', path: './commands/gap-report.mjs', register: 'registerGapReport'},
  {name: 'upgrade', path: './commands/upgrade.mjs', register: 'registerUpgrade'},
  {name: 'theme', path: './commands/build-theme.mjs', register: 'registerTheme'},
  {name: 'discover', path: './commands/discover.mjs', register: 'registerDiscover'},
];

for (const cmd of commandModules) {
  try {
    const mod = await import(path.join(CLI_SRC, cmd.path));
    mod[cmd.register](program);
  } catch (e) {
    console.warn(`Warning: could not load ${cmd.name}: ${e.message}`);
    program.command(cmd.name).description(`(failed to load)`);
  }
}

// ── Extract metadata from Commander's internal tree ─────────────────

function extractOption(opt) {
  return {
    flags: opt.flags,
    description: opt.description,
    ...(opt.defaultValue !== undefined && opt.defaultValue !== false
      ? {defaultValue: String(opt.defaultValue)}
      : {}),
    required: opt.required || false,
  };
}

function extractCommand(cmd) {
  const args = (cmd._args || cmd.registeredArguments || []).map(a => ({
    name: a.name(),
    required: a.required,
    variadic: a.variadic || false,
    description: a.description || '',
  }));

  const options = cmd.options
    .filter(o => !['--version', '-V', '--help', '-h'].some(f => o.flags.includes(f)))
    .map(extractOption);

  const subcommands = cmd.commands
    .filter(sub => !sub._hidden)
    .map(extractCommand);

  return {
    name: cmd.name(),
    description: cmd.description() || '',
    arguments: args,
    options,
    subcommands,
  };
}

const globalOptions = program.options
  .filter(o => !['--version', '-V', '--help', '-h'].some(f => o.flags.includes(f)))
  .map(extractOption);

const commands = program.commands
  .filter(c => !c._hidden && c.name() !== 'help')
  .map(extractCommand);

// ── Write output ────────────────────────────────────────────────────

fs.mkdirSync(OUT_DIR, {recursive: true});

const content = `// Auto-generated by scripts/generate-cli-registry.mjs — do not edit

export const CLI_VERSION = ${JSON.stringify(CLI_PKG.version)};

export type CliOption = {
  flags: string;
  description: string;
  defaultValue?: string;
  required: boolean;
};

export type CliArgument = {
  name: string;
  required: boolean;
  variadic: boolean;
  description: string;
};

export type CliCommand = {
  name: string;
  description: string;
  arguments: CliArgument[];
  options: CliOption[];
  subcommands: CliCommand[];
};

export const globalOptions: CliOption[] = ${JSON.stringify(globalOptions, null, 2)};

export const commands: CliCommand[] = ${JSON.stringify(commands, null, 2)};
`;

fs.writeFileSync(OUT_FILE, content, 'utf-8');
console.log(
  `Wrote ${commands.length} command(s) + ${globalOptions.length} global option(s) to ${path.relative(REPO_ROOT, OUT_FILE)}`,
);
